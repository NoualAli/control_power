<template>
    <div v-if="can('view_familly')">
        <ContentHeader>
            <template #actions>
                <router-link v-if="can('create_familly')" :to="{ name: 'famillies-create' }" class="btn btn-info">
                    Ajouter
                </router-link>
            </template>
        </ContentHeader>
        <ContentBody>
            <NLDatatable :columns="columns" :actions="actions" title="Liste des familles" urlPrefix="famillies" @edit="edit"
                @delete="destroy" />
        </ContentBody>
    </div>
</template>

<script>
import NLDatatable from '../../../components/Datatable/NLDatatable'
export default {
    components: { NLDatatable },
    layout: 'MainLayout',
    middleware: [ 'auth', 'admin' ],
    metaInfo() {
        return { title: 'Famille' }
    },
    data() {
        return {
            reloadData: false,
            columns: [
                {
                    label: 'Nom',
                    field: 'name',
                    sortable: true
                },
                {
                    label: 'Nombre de domaines',
                    field: 'domains_count'
                }
            ],
            actions: {
                edit: (item) => {
                    return this.can('edit_familly')
                },
                delete: (item) => {
                    return this.can('delete_familly')
                }
            }
        }
    },
    methods: {
        /**
         * Redirige vers la page d'edition
         * @param {Object} item
         */
        edit(item) {
            this.$router.push({ name: 'famillies-edit', params: { familly: item.id } })
        },

        /**
         * Supprime la ressource
         * @param {Object} item
         */
        destroy(item) {
            this.$swal.confirm_destroy().then((action) => {
                if (action.isConfirmed) {
                    api.delete('famillies/' + item.id).then(response => {
                        if (response.data.status) {
                            this.initData()
                            this.$swal.toast_success(response.data.message)
                        } else {
                            this.$swal.toast_error(response.data.message)
                        }
                    })
                }
            }).catch(error => {
                this.$swal.alert_error()
            })
        },

        /**
         * Initialise les donn√©es
         */
        initData() {
            this.reloadData = true
        }
    }
}
</script>
